	/**
	 * Set BankBalance Identification  Value
	 * @param int|array $value
	 * @param array[int]int $key List Of Primary Key.
	 * @param array[int]string $type  List Of Type.0 As 'single' 1 As 'array'
	 */
	public function setBankBalanceId($value, $key, $type) {
		if ($type == 'single') {
			$this->bankBalanceId = $value;
		} else if ($type == 'array') {
			$this->bankBalanceId [$key] = $value;
		} else {
			echo json_encode(array("success" => false, "message" => "Cannot Identifiy Type String Or Array:setBankBalanceId ?"));
			exit();
		}
	}

	/**
	 * Return BankBalance Identification  Value
	 * @param array[int]int $key List Of Primary Key.
	 * @param array[int]string $type  List Of Type.0 As 'single' 1 As 'array'
	 * @return bool|array
	 */
	public function getBankBalanceId($key, $type) {
		if ($type == 'single') {
			return $this->bankBalanceId;
		} else if ($type == 'array') {
			return $this->bankBalanceId [$key];
		} else {
			echo json_encode(array("success" => false, "message" => "Cannot Identifiy Type String Or Array:getBankBalanceId ?"));
			exit();
		}
	}


